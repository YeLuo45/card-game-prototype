'use strict';

(function () {

  function CodeGenerator(options) {
    options = options || {};
    this.language = options.language || 'javascript';
    this.indent = options.indent || '  ';
    this.quote = options.quote || "'";
    this.modules = {};
    this.metrics = { generated: 0, lines: 0 };
  }

  CodeGenerator.prototype.setLanguage = function (lang) {
    this.language = lang;
    return { success: true };
  };

  CodeGenerator.prototype.setIndent = function (indent) {
    this.indent = indent;
    return { success: true };
  };

  CodeGenerator.prototype._formatValue = function (v) {
    if (v === null) return 'null';
    if (v === undefined) return 'undefined';
    if (typeof v === 'string') return this.quote + v.replace(/'/g, "\\'") + this.quote;
    if (typeof v === 'number' || typeof v === 'boolean') return String(v);
    if (Array.isArray(v)) return '[' + v.map(this._formatValue.bind(this)).join(', ') + ']';
    if (typeof v === 'object') {
      var pairs = [];
      for (var k in v) {
        if (Object.prototype.hasOwnProperty.call(v, k)) {
          pairs.push(k + ': ' + this._formatValue(v[k]));
        }
      }
      return '{ ' + pairs.join(', ') + ' }';
    }
    return String(v);
  };

  // ---- Module ----
  CodeGenerator.prototype.generateModule = function (config) {
    config = config || {};
    var name = config.name || 'module';
    var body = config.body || [];
    var exports = config.exports || [];
    var lines = [];
    lines.push('// ' + name + '.js');
    lines.push('// Auto-generated by CodeGenerator');
    lines.push('');
    if (config.header) lines.push(config.header);
    lines.push('');
    // body
    for (var i = 0; i < body.length; i++) {
      lines.push(body[i]);
    }
    lines.push('');
    // exports
    if (exports.length > 0) {
      lines.push('module.exports = {');
      for (var j = 0; j < exports.length; j++) {
        var e = exports[j];
        var comma = j < exports.length - 1 ? ',' : '';
        if (typeof e === 'string') {
          lines.push(this.indent + e + comma);
        } else if (e.name) {
          lines.push(this.indent + e.name + (e.value ? ': ' + this._formatValue(e.value) : '') + comma);
        }
      }
      lines.push('};');
    }
    this.metrics.generated++;
    this.metrics.lines += lines.length;
    return lines.join('\n');
  };

  // ---- Class ----
  CodeGenerator.prototype.generateClass = function (config) {
    config = config || {};
    var className = config.name || 'MyClass';
    var parent = config.extends || config.parent;
    var methods = config.methods || [];
    var properties = config.properties || [];
    var lines = [];
    lines.push('/**');
    lines.push(' * ' + className);
    if (config.description) lines.push(' * ' + config.description);
    lines.push(' */');
    lines.push('function ' + className + (parent ? ' extends ' + parent : '') + ' (options) {');
    lines.push(this.indent + 'options = options || {};');
    for (var p = 0; p < properties.length; p++) {
      var prop = properties[p];
      var val = prop.value !== undefined ? this._formatValue(prop.value) : 'null';
      lines.push(this.indent + 'this.' + prop.name + ' = ' + val + ';');
    }
    lines.push('}');
    for (var m = 0; m < methods.length; m++) {
      var method = methods[m];
      var sig = (method.static ? 'static ' : '') + (method.name || 'method') + '(' + (method.params || []).join(', ') + ')';
      lines.push(className + '.prototype.' + method.name + ' = function (' + (method.params || []).join(', ') + ') {');
      if (method.body) {
        var bodyLines = method.body.split('\n');
        for (var bl = 0; bl < bodyLines.length; bl++) {
          if (bodyLines[bl].trim()) lines.push(this.indent + bodyLines[bl]);
        }
      }
      lines.push('};');
      lines.push('');
    }
    this.metrics.generated++;
    this.metrics.lines += lines.length;
    return lines.join('\n');
  };

  // ---- Function ----
  CodeGenerator.prototype.generateFunction = function (config) {
    config = config || {};
    var name = config.name || 'fn';
    var params = config.params || [];
    var lines = [];
    lines.push('function ' + name + ' (' + params.join(', ') + ') {');
    if (config.body) {
      var bodyLines = config.body.split('\n');
      for (var i = 0; i < bodyLines.length; i++) {
        if (bodyLines[i].trim()) lines.push(this.indent + bodyLines[i]);
      }
    }
    lines.push('}');
    this.metrics.generated++;
    this.metrics.lines += lines.length;
    return lines.join('\n');
  };

  // ---- Interface (JSDoc typedef) ----
  CodeGenerator.prototype.generateInterface = function (config) {
    config = config || {};
    var name = config.name || 'MyInterface';
    var fields = config.fields || [];
    var lines = [];
    lines.push('/**');
    lines.push(' * @typedef {Object} ' + name);
    for (var i = 0; i < fields.length; i++) {
      var f = fields[i];
      lines.push(' * @property {' + (f.type || '*') + '} ' + f.name + (f.required ? '' : ' [optional]') + (f.description ? ' - ' + f.description : ''));
    }
    lines.push(' */');
    this.metrics.generated++;
    this.metrics.lines += lines.length;
    return lines.join('\n');
  };

  // ---- CRUD service ----
  CodeGenerator.prototype.generateCrud = function (config) {
    config = config || {};
    var entity = config.name || 'Item';
    var fields = config.fields || [];
    var lines = [];
    lines.push('// CRUD service for ' + entity);
    lines.push('var ' + entity.toLowerCase() + 's = [];');
    lines.push('var nextId = 1;');
    lines.push('');
    // create
    lines.push('function create' + entity + '(data) {');
    lines.push(this.indent + 'var item = { id: nextId++ };');
    for (var i = 0; i < fields.length; i++) {
      lines.push(this.indent + 'item.' + fields[i] + ' = data.' + fields[i] + ';');
    }
    lines.push(this.indent + entity.toLowerCase() + 's.push(item);');
    lines.push(this.indent + 'return item;');
    lines.push('}');
    lines.push('');
    // read
    lines.push('function get' + entity + '(id) {');
    lines.push(this.indent + 'return ' + entity.toLowerCase() + 's.find(function (x) { return x.id === id; });');
    lines.push('}');
    lines.push('');
    lines.push('function list' + entity + 's() {');
    lines.push(this.indent + 'return ' + entity.toLowerCase() + 's.slice();');
    lines.push('}');
    lines.push('');
    // update
    lines.push('function update' + entity + '(id, data) {');
    lines.push(this.indent + 'var item = get' + entity + '(id);');
    lines.push(this.indent + 'if (!item) return null;');
    lines.push(this.indent + 'Object.assign(item, data);');
    lines.push(this.indent + 'return item;');
    lines.push('}');
    lines.push('');
    // delete
    lines.push('function delete' + entity + '(id) {');
    lines.push(this.indent + 'var idx = ' + entity.toLowerCase() + 's.findIndex(function (x) { return x.id === id; });');
    lines.push(this.indent + 'if (idx === -1) return false;');
    lines.push(this.indent + entity.toLowerCase() + 's.splice(idx, 1);');
    lines.push(this.indent + 'return true;');
    lines.push('}');
    lines.push('');
    lines.push('module.exports = { create' + entity + ', get' + entity + ', list' + entity + 's, update' + entity + ', delete' + entity + ' };');
    this.metrics.generated++;
    this.metrics.lines += lines.length;
    return lines.join('\n');
  };

  // ---- Batch generation ----
  CodeGenerator.prototype.generateBatch = function (configs, type) {
    if (!Array.isArray(configs)) return { error: 'invalid_input' };
    var results = [];
    for (var i = 0; i < configs.length; i++) {
      var fn = this['generate' + type.charAt(0).toUpperCase() + type.slice(1)];
      if (typeof fn === 'function') {
        results.push({ name: configs[i].name, code: fn.call(this, configs[i]) });
      }
    }
    return results;
  };

  // ---- File generation (composes module + class) ----
  CodeGenerator.prototype.generateFile = function (config) {
    config = config || {};
    var lines = [];
    if (config.comment) lines.push('// ' + config.comment);
    if (config.class) lines.push(this.generateClass(config.class));
    if (config.functions) {
      for (var i = 0; i < config.functions.length; i++) {
        lines.push(this.generateFunction(config.functions[i]));
      }
    }
    if (config.exports) {
      lines.push('module.exports = {');
      for (var k = 0; k < config.exports.length; k++) {
        var e = config.exports[k];
        var comma = k < config.exports.length - 1 ? ',' : '';
        if (typeof e === 'string') {
          lines.push(this.indent + e + comma);
        } else {
          lines.push(this.indent + e.name + comma);
        }
      }
      lines.push('};');
    }
    this.metrics.generated++;
    this.metrics.lines += lines.length;
    return lines.join('\n');
  };

  CodeGenerator.prototype.getMetrics = function () {
    return JSON.parse(JSON.stringify(this.metrics));
  };

  CodeGenerator.prototype.clear = function () {
    this.metrics = { generated: 0, lines: 0 };
    return { success: true };
  };

  if (typeof window !== 'undefined') {
    window.CodeGenerator = CodeGenerator;
  }
  if (typeof module !== 'undefined' && module.exports) {
    module.exports = { CodeGenerator: CodeGenerator };
  }
})();
